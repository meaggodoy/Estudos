#Escreva um algoritmo para ler as dimensões de um retângulo (base e altura), calcular e escrever a
#área do retângulo.

print("Vamos calcular a área de um retângulo!")
base = float(input("Digite o tamanho da base: "))
altura = float(input("Digite o tamanho da altura: "))
area = base * altura
print("A área do retângulo é de: ", area)
